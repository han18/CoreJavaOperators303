
public class LogicalOperatorsDemo {
   public static void main(String[] args)
   {
       boolean x = true;
       boolean y = false;
       System.out.println("x & y : " + (x & y)); // & in this case is a bitwise
       System.out.println("x && y : " + (x && y));
       System.out.println("x | y : " + (x | y));
       System.out.println("x || y: " + (x || y));
       System.out.println("x ^ y : " + (x ^ y));
       System.out.println("!x : " + (!x));
   }
}

